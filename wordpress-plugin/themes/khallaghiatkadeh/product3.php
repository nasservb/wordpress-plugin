مرحله شتابدهی، نواندیشان را با کسب و کار، تجارت، ایده پردازی و مفاهیم کارآفرینی آشنا می‌کند. آشنایی آنها با عرصه کارآفرینی، می‌تواند در مسیر زندگی آنها مؤثر باشد. چرا که آشنایی با این مفاهیم نگرش آنها نسبت به آینده را دگرگون می سازد. می‌توانند شغلی که در آینده تصمیم به فعالیت در آن دارند را تصور کنند و در همین راه رشته تحصیلی مرتبطی را برگزینند .این آموزه­ها نکاتی هستند که معمولا افراد در تحصیلات  اکادمیک  با آن آشنا می شوند، اما اگر این آشنایی از دوره‌های پایین‌تر صورت گیرد زمینه ساز برای آینده خواهد بود. این مرحله می‌تواند زمینه ساز حصول اطمینان از حضور نواندیشان در عرصه نوآوری و خلاقیت باشد و با دریافت خروجی مناسب از ایده های آنان، زمینه فرهنگسازی و آشناسازی دانش آموزان با توسعه اقتصاد از طریق ایجاد شرکت‌های دانش‌بنیان مبتنی بر همین ایده‌ها پس از دوران تحصیلی فراهم کند. این مرحله شامل دو ترم مهارت های کسب وکار و کارآفرینی است.</p>
